   subroutine legndr(x,p,np)
   !--------------------------------------------------------------------
   ! Generate Legendre polynomials at x by recursion.
   ! Place p(subl) in p(l+1).
   !--------------------------------------------------------------------
   ! externals
   real(kr)::x
   real(kr)::p(*)
   integer::np
   ! internals
   integer::m1,i
   real(kr)::g,h

   p(1)=1
   p(2)=x
   if (np.lt.2) return
   m1=np-1
   do i=1,m1
      g=x*p(i+1)
      h=g-p(i)
      p(i+2)=h+g-h/(i+1)
   enddo
   return
   end subroutine legndr

